package Operator;

public class RelationalOperator {
	
	public static void main(String[] args) {
		
		int x = 8;
		int y = 10;
		
		System.out.print(x == y);
		System.out.print(x != y);
		System.out.print(x < y);
		System.out.print(x > y);
		System.out.print(x <= y);
		System.out.print(x >= y);
	
	}

	
		
		
	

}
